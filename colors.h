// 基础前景色
#define BLACK   "\033[30m"
#define RED     "\033[31m"
#define GREEN   "\033[32m"
#define YELLOW  "\033[33m"
#define BLUE    "\033[34m"
#define PURPLE  "\033[35m"  // 标准名称应为MAGENTA
#define CYAN    "\033[36m"
#define WHITE   "\033[37m"

// 亮色模式（加;1）
#define L_RED    "\033[1;31m"
#define L_GREEN  "\033[1;32m"
#define L_YELLOW "\033[1;33m"
#define L_BLUE   "\033[1;34m"
#define L_PURPLE "\033[1;35m"
#define L_CYAN   "\033[1;36m"
#define L_WHITE  "\033[1;37m"

// 背景色
#define B_BLACK  "\033[40m"
#define B_RED    "\033[41m"
#define B_GREEN  "\033[42m"
#define B_YELLOW "\033[43m"
#define B_BLUE   "\033[44m"
#define B_PURPLE "\033[45m"
#define B_CYAN   "\033[46m"
#define B_WHITE  "\033[47m"

#define DARKGRAY "\033[90m"  

// 特殊效果
#define BOLD      "\033[1m"
#define UNDERLINE "\033[4m"
#define BLINK     "\033[5m"
#define REVERSE   "\033[7m"

// 重置所有样式
#define ORIGINAL  "\033[0m"
